import Base_weight

# Загружаем существующие веса
weight_obj = Base_weight.WordWeight("")
weight_obj.text = []

PASSES = 30
all_sentences = PHRASES + QA_PAIRS
print(f"\nОбучение весов: {PASSES} проходов по {len(all_sentences)} фразам...")

for p in range(PASSES):
    for sentence in all_sentences:
        words = weight_obj.clean(sentence)
        for w in words:
            weight_obj.text = [w]
            weight_obj.update()
    if (p + 1) % 10 == 0:
        print(f"  [{p+1}/{PASSES}] проходов завершено")

weight_obj.save()
print("Веса сохранены.")

